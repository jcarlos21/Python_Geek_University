# usando a estrutura for:
for i in range(0, 100):
    print(i+1)
# usando a estrutura while:
j = 0
while j < 100:
    print(j+1)
    j += 1
else:
    print('Exiting the while loop')
# usando a estrutua do while (apenas simulação, a estrtura do while não existe em Python):
g = 0
while True:
    print(g+1)
    g += 1
    if g == 100:
        break
